#!/bin/bash

# Скрипт для инициализации базы данных при первом развертывании на Render
# Этот скрипт можно использовать для автоматического создания таблиц

set -e

echo "=== Инициализация базы данных ==="

# Проверяем наличие PHP
if ! command -v php &> /dev/null
then
    echo "PHP не найден!"
    exit 1
fi

# Переходим в директорию проекта
cd "$(dirname "$0")"

echo "Директория проекта: $(pwd)"

# Загружаем конфигурацию БД
php -r "
\$config = include 'config/database.php';
echo 'Подключение к БД: ' . \$config['host'] . ':' . \$config['port'] . '/' . \$config['database'] . PHP_EOL;

// Здесь можно добавить логику инициализации БД
// Например, импорт schema.sql
"

echo "Инициализация завершена!"
